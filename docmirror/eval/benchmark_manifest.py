"""Versioned benchmark results — published as CI artifacts for transparency.

The ``BenchmarkManifest`` is the single source of truth for DocMirror's
published accuracy numbers. It is generated by CI on every release and
can be compared across releases to surface regressions.

Usage::

    from docmirror.eval.benchmark_manifest import BenchmarkManifest, BenchmarkRecord

    manifest = BenchmarkManifest.build(
        release_tag="v1.0.0",
        records=[...],
    )
    manifest.to_json("docs/benchmarks/results/v1.0.0.json")

Design (GA1.0-EC-01 §Component 4):
    - One canonical manifest format, consumed by README badge generation
      and the comparison table builder.
    - Every release produces a versioned manifest file.
    - Golden cases are stable across releases, so records are comparable.
"""

from __future__ import annotations

import json
from dataclasses import asdict, dataclass, field
from datetime import datetime, timezone
from typing import Any

from docmirror import __version__ as docmirror_version
from docmirror.eval.golden_loader import GoldenCase
from docmirror.models.entities.quality_report import ParseQualityReport


@dataclass
class BenchmarkRecord:
    """One row in the benchmark result table.

    Each record corresponds to one (GoldenCase, parse_mode) combination.
    Metrics are extracted from the ``ParseQualityReport`` produced by
    ``compute_metrics()``.
    """

    golden_case_id: str
    document_type: str
    format_: str  # "pdf", "image", "docx", etc.
    mode: str  # "auto", "fast", "balanced", "accurate"

    # Core quality metrics (from ParseQualityReport.metrics dict)
    table_f1: float = 0.0
    text_f1: float = 0.0
    kv_f1: float = 0.0
    char_preservation_rate: float = 0.0
    reading_order_score: float = 0.0
    provenance_coverage: float = 0.0
    table_structure_score: float = 0.0
    domain_field_f1: float = 0.0

    # Performance
    elapsed_ms: float = 0.0
    page_count: int = 0

    # Versioning
    docmirror_version: str = docmirror_version
    timestamp: str = field(default_factory=lambda: datetime.now(timezone.utc).isoformat())

    # Audit
    gate_passed: bool = False
    warnings: list[str] = field(default_factory=list)

    @classmethod
    def from_report(
        cls,
        golden_case: GoldenCase,
        report: ParseQualityReport,
        *,
        mode: str = "auto",
    ) -> BenchmarkRecord:
        """Build a record from a *golden_case* and its *report*.

        Args:
            golden_case: The golden case that was parsed.
            report: The quality report from ``compute_metrics()``.
            mode: The parse mode used.

        Returns:
            A populated ``BenchmarkRecord``.
        """
        metrics = report.metrics or {}
        fmt = golden_case.file_path.suffix.lstrip(".") if golden_case.file_path else "pdf"
        record = cls(
            golden_case_id=golden_case.case_id or golden_case.name or "unknown",
            document_type=golden_case.document_type or "unknown",
            format_=fmt,
            mode=mode,
            table_f1=metrics.get("table_f1", 0.0),
            text_f1=metrics.get("text_f1", 0.0),
            kv_f1=metrics.get("kv_field_f1", 0.0),
            char_preservation_rate=metrics.get("char_preservation_rate", 0.0),
            reading_order_score=metrics.get("reading_order_score", 0.0),
            provenance_coverage=metrics.get("provenance_coverage", 0.0),
            table_structure_score=metrics.get("table_structure_score", 0.0),
            domain_field_f1=metrics.get("domain_field_f1", 0.0),
            elapsed_ms=metrics.get("elapsed_ms", 0.0),
            page_count=int(metrics.get("page_count", 0)),
            gate_passed=report.gate_passed or False,
            warnings=report.warnings or [],
        )
        return record


@dataclass
class BenchmarkManifest:
    """Versioned benchmark result for one release.

    Serialized to JSON and published as a CI artifact. Consumers include:
    - The README badge generator (overall F1 → badge)
    - The comparison table builder (side-by-side with competitors)
    - The regression delta checker (diff with previous release)
    """

    release_tag: str
    generated_at: str = field(default_factory=lambda: datetime.now(timezone.utc).isoformat())
    records: list[BenchmarkRecord] = field(default_factory=list)

    # Optional: baseline comparison
    baseline_release: str | None = None
    regression_deltas: dict[str, float] = field(default_factory=dict)

    def add_record(self, record: BenchmarkRecord) -> None:
        """Add a single benchmark record."""
        self.records.append(record)

    def add_records(self, records: list[BenchmarkRecord]) -> None:
        """Add multiple benchmark records."""
        self.records.extend(records)

    @property
    def summary(self) -> dict[str, float]:
        """Aggregate metrics across all records.

        Returns a dict of average F1 scores, average latency, etc.
        """
        if not self.records:
            return {"record_count": 0}

        n = len(self.records)
        return {
            "record_count": n,
            "avg_table_f1": round(sum(r.table_f1 for r in self.records) / n, 4),
            "avg_text_f1": round(sum(r.text_f1 for r in self.records) / n, 4),
            "avg_kv_f1": round(sum(r.kv_f1 for r in self.records) / n, 4),
            "avg_char_preservation": round(sum(r.char_preservation_rate for r in self.records) / n, 4),
            "avg_elapsed_ms": round(sum(r.elapsed_ms for r in self.records) / n, 1),
            "gate_pass_rate": round(sum(1 for r in self.records if r.gate_passed) / n, 4),
        }

    def to_dict(self) -> dict[str, Any]:
        """Serialize manifest to a JSON-safe dict.

        The dict structure follows the published schema at
        ``docs/schemas/benchmarks/v1.0.schema.json``.
        """
        return {
            "manifest_version": "1.0",
            "release_tag": self.release_tag,
            "generated_at": self.generated_at,
            "baseline_release": self.baseline_release,
            "summary": self.summary,
            "regression_deltas": self.regression_deltas or None,
            "records": [asdict(r) for r in self.records],
        }

    def to_json(self, path: str | None = None, indent: int = 2) -> str:
        """Serialize to JSON string.

        Args:
            path: Optional file path to write.
            indent: JSON indentation level.

        Returns:
            JSON string.
        """
        data = self.to_dict()

        # Convert format_ to match JSON field naming
        for record in data["records"]:
            record["format"] = record.pop("format_")

        json_str = json.dumps(data, indent=indent, ensure_ascii=False, default=str)
        if path:
            import os

            os.makedirs(os.path.dirname(path) or ".", exist_ok=True)
            with open(path, "w") as f:
                f.write(json_str)
        return json_str

    @classmethod
    def from_dict(cls, data: dict[str, Any]) -> BenchmarkManifest:
        """Deserialize from a dict (from JSON)."""
        records = []
        for r in data.get("records", []):
            r["format_"] = r.pop("format", r.get("format_", "pdf"))
            records.append(BenchmarkRecord(**r))

        manifest = cls(
            release_tag=data.get("release_tag", "unknown"),
            generated_at=data.get("generated_at", ""),
            baseline_release=data.get("baseline_release"),
            records=records,
        )
        if data.get("regression_deltas"):
            manifest.regression_deltas = data["regression_deltas"]
        return manifest

    @classmethod
    def from_json(cls, path: str) -> BenchmarkManifest:
        """Load from a JSON file."""
        with open(path) as f:
            data = json.load(f)
        return cls.from_dict(data)

    @classmethod
    def build(
        cls,
        release_tag: str,
        records: list[BenchmarkRecord],
        *,
        baseline: BenchmarkManifest | None = None,
    ) -> BenchmarkManifest:
        """Build a complete manifest from records.

        If *baseline* is provided, compute regression deltas.

        Args:
            release_tag: Git tag or version string.
            records: List of benchmark records.
            baseline: Optional previous manifest for regression comparison.

        Returns:
            A ``BenchmarkManifest`` with summary and optional deltas.
        """
        manifest = cls(release_tag=release_tag, records=records)

        if baseline:
            manifest.baseline_release = baseline.release_tag
            current_summary = manifest.summary
            baseline_summary = baseline.summary
            deltas: dict[str, float] = {}
            for key in current_summary:
                if key in baseline_summary and key != "record_count":
                    delta = round(current_summary[key] - baseline_summary[key], 4)
                    deltas[f"{key}_delta"] = delta
            manifest.regression_deltas = deltas

        return manifest


__all__ = [
    "BenchmarkRecord",
    "BenchmarkManifest",
]
